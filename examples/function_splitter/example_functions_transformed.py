def 输出结果_1():
    
    # 输出结果
    print(f"x = {x}")
    print(f"y = {y}")
    print(f"z = {z}")
def 初始化数组():
    # 初始化数组
    numbers = []
def 填充数据():
    
    # 填充数据
    for i in range(10):
        numbers.append(i * i)
def 计算总和():
    
    # 计算总和
    total = sum(numbers)
def 计算平均值():
    
    # 计算平均值
    average = total / len(numbers)
def 打印结果():
    
    # 打印结果
    print(f"Numbers: {numbers}")
    print(f"Total: {total}")
    print(f"Average: {average}")